package basicConcept;

public class DataTypes {

	// comment:
	// two type
	// 1. single comment://
	// 2. Multiple comment:/* hi*/

	public static void main(String[] args) {
		// Primitive data types: int , double, char, boolean
		// int:
		int i = 10;
		int j = 20;
		int k = 20;
		System.out.println(i);
		System.out.println(i + j);
		// 2.double
		double d = 12.33;
		double c = 34.45;
		double d2 = 100;// this will treat like 100.00
		System.out.println(d2);

		// 3. char: single digit value : should be return within single quotes
		char c0 = 'A';
		char c1 = 'B';
		char c3 = '1';
		System.out.println(c3);

		// 4. boolean: true and false is value but in these are also keyword
		// available in java
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(true);

		// 5 String is a class not a data type
		String s1 = "Hello world";
		String s2 = "my name is khan";
		String s3 = "1000";
		String s4 = "100.00";
		System.out.println(s4);

	}

}
